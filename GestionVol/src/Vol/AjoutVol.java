    public String suggestion_Id(String deb){
		int i=1;
		StringBuilder id=new StringBuilder();
		id.append(deb);
		id.append(i);
		while(already_exist(id.toString())) {
			id.delete(0, id.length());
			i++;
			id.append(deb);
			id.append(i);
		}
		return id.toString();
    }
    private boolean already_exist(String num){
        boolean ret=false;
        try{
            pst=con.prepareStatement("SELECT * FROM VOL WHERE NumVol='"+num+"'");
            rs=pst.executeQuery();
            if(rs.next()){
               ret=true;
            }
            pst.close();
            rs.close();
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, e);
        }
        return ret;
    }
    
    private boolean valid_frais(String montant){
        boolean ret=false;
        if(montant.matches("[+-]?\\d*(\\.\\d+)?")){
            ret=true;
        }else{
            JOptionPane.showMessageDialog(null ,"Le montant doit être composé de chiffre");
        }
        return ret;
    }

