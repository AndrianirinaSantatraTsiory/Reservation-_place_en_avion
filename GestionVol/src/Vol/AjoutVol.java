    private void valider_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_valider_btnActionPerformed
        String numVol=NumVol_form.getText();
        String depart=depart_form.getSelectedItem().toString();
        String arrivee=arrivee_form.getSelectedItem().toString();
        Double frais=0.0;
        if(NumVol_form.getText().equals("")||frais_form.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Tous les champs sont obligatoires");
        }else{
            
            if(already_exist(numVol)){
                JOptionPane.showMessageDialog(null,"Le numero est déjà utilisé");
            }
            else{
                if(depart_form.getSelectedItem().toString().equals(arrivee_form.getSelectedItem().toString())){
                    JOptionPane.showMessageDialog(null,"La ville de départ ne peut pas être la même que la ville d'arrivée");
                }
                else{
                    if(valid_frais(frais_form.getText())){
                        frais=Double.parseDouble(frais_form.getText());
                        try{
                            pst=con.prepareStatement("INSERT INTO VOL(NumVol,VilleDepart,VilleArrivée,frais) VALUES (?,?,?,?)");
                            pst.setString(1, NumVol_form.getText());
                            pst.setString(2, depart_form.getSelectedItem().toString());
                            pst.setString(3, arrivee_form.getSelectedItem().toString());
                            pst.setDouble(4,frais);
                            pst.executeUpdate();
                            pst.close();
                            JOptionPane.showMessageDialog(null,"Ajout avec succès");
                            vol.volList();
                            dispose();
                        }catch(Exception e){
                            JOptionPane.showMessageDialog(null, e);
                        }
                    }
                }
            }
        }    
    }//GEN-LAST:event_valider_btnActionPerformed

