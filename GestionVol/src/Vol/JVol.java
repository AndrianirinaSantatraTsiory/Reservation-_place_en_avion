   public void volList(){
        String req="SELECT * FROM VOL";
        try{
            pst=con.prepareStatement(req);
            rs=pst.executeQuery();
            //int i=1;
            DefaultTableModel model=new DefaultTableModel(new String[]{
               "N°VOL","VILLE DE DEPART","VILLE D'ARRIVEE","FRAIS" 
            },0){
                public boolean isCellEditable(int row ,int column){
                    return false;
                }
            };
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getString("NumVol"),
                    rs.getString("VilleDepart"),
                    rs.getString("VilleArrivée"),
                    rs.getDouble("frais"),
                });
                //i++;
            }
            pst.close();
            rs.close();
            Vol_table.setModel(model);
            Vol_table.setRowHeight(25);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
