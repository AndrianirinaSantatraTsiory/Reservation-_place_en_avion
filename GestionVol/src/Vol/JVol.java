    private void supprimer_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimer_btnActionPerformed
        if(Vol_table.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null,"Veuillez selectionner le vol à supprimér");
        }
        else{
            int opt=JOptionPane.showConfirmDialog(null,"Voulez vous vraiment le supprimer","supprimer",JOptionPane.YES_NO_OPTION);
            if(opt==0){
                String num=String.valueOf(Vol_table.getValueAt(Vol_table.getSelectedRow(),0));
                String req="DELETE FROM VOL WHERE NumVol='"+num+"'";
                try{
                    pst=con.prepareStatement(req);
                    pst.executeUpdate();
                    pst.close();
                    JOptionPane.showMessageDialog(null,"Suppression effectué");
                    volList();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,e);
                }
            }
        }
    }//GEN-LAST:event_supprimer_btnActionPerformed

    private void editer_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editer_btnActionPerformed
        if(Vol_table.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null,"Veuillez selectionner le vol à modifier");
        }else{
            String num=String.valueOf(Vol_table.getValueAt(Vol_table.getSelectedRow(),0));
            String dep=String.valueOf(Vol_table.getValueAt(Vol_table.getSelectedRow(),1));
            String arr=String.valueOf(Vol_table.getValueAt(Vol_table.getSelectedRow(),2));
            String frais=String.valueOf(Vol_table.getValueAt(Vol_table.getSelectedRow(),3));
            EditerVol edit=new EditerVol(num,dep,arr,Double.parseDouble(frais),this);
            edit.show();
            
        }
    }//GEN-LAST:event_editer_btnActionPerformed

