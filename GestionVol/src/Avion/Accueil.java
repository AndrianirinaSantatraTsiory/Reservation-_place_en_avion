    private void histogramme_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_histogramme_btnActionPerformed
        String annee=JOptionPane.showInputDialog("Veuillez entrer un ann√©e:");
        if(!annee.equals("")){
            String req="SELECT  SUM(VOL.frais) AS recette, AVION.NumAvion FROM AVION,RESERVATION,VOL WHERE (VOL.NumVol=AVION.NumVol) AND (AVION.NumAVion=RESERVATION.NumAvion) AND (STRFTIME(\"%Y\",RESERVATION.Date_Reservation))=\""+annee+"\" GROUP BY AVION.NumAVION";
            DefaultCategoryDataset dataset=new DefaultCategoryDataset();
            try {
                pst=con.prepareStatement(req);
                rs=pst.executeQuery();
                while(rs.next()){
                    dataset.setValue(rs.getDouble("recette"),"RECETTE",rs.getString("NumAvion"));
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,ex);
            }
        
        
            JFreeChart chart=ChartFactory.createBarChart("RECETTE ANNUELLE PAR AVION", "Numero Avion", "Recette", dataset, PlotOrientation.VERTICAL, false, true, false);
        //CategoryPlot p=chart.getCategoryPlot();
        //p.setRangeGridlinePaint(Color.red);
            ChartFrame frame=new ChartFrame("RECETTE ANNUELLE PAR AVION",chart);
            frame.setSize(450,350);
            frame.setLocationRelativeTo(null);

            frame.setVisible(true);
        }
        
    }//GEN-LAST:event_histogramme_btnActionPerformed

