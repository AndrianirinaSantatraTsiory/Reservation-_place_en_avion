    private void Valider_modif_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Valider_modif_btnActionPerformed
        boolean exist=false;
        int opt=JOptionPane.showConfirmDialog(null,"Voulez vous vraiment le modifier","supprimé",JOptionPane.YES_NO_OPTION);
        if(opt==0){
            if(NumReservation_form.getText().equals("")||NumAvion_form.getText().equals("")||NumPlace_form.getText().equals("")||((JTextField)DateReservation_form.getDateEditor().getUiComponent()).getText().isEmpty()||NomVoyageur_form.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Tous les champs sont obligatoires");
            }else{
                if(!old_NumRes.equals(NumReservation_form.getText())){
                    if(already_exist(NumReservation_form.getText())){
                        exist=true;
                        JOptionPane.showMessageDialog(null,"Identifiant dejà utilisé par une autre reservation");
                    }
                }
  
                    if(!exist&&Validation_Avion()&&Validation_Place()){
                        try{
                            SimpleDateFormat sp=new SimpleDateFormat("yyyy-MM-dd");
                            pst=con.prepareStatement("UPDATE RESERVATION SET NumReservation=?, NumAvion=?, NumPlace=?,Date_Reservation=?, NomVoyageur=? WHERE NumReservation=?");
                            pst.setString(1, NumReservation_form.getText());
                            pst.setString(2, NumAvion_form.getText());
                            pst.setString(3, NumPlace_form.getText());
                            pst.setString(4,sp.format(DateReservation_form.getDate()));
                            pst.setString(5, NomVoyageur_form.getText());
                            pst.setString(6, old_NumRes);
                            pst.executeUpdate();
                            if(!old_NumRes.equals(NumReservation_form.getText())){
                                     pst=con.prepareStatement("UPDATE PLACE SET NumReservation='"+NumReservation_form.getText()+"' WHERE NumAvion='"+old_NumAvion+"' and NumPlace='"+old_NumPlace+"'");
                                     pst.executeUpdate();
                                     pst.close();
                            }
                            if(!old_NumPlace.equals(NumPlace_form.getText())||!old_NumAvion.equals(NumAvion_form.getText())){
                                 pst=con.prepareStatement("UPDATE PLACE SET Occupation=1, NumReservation='"+NumReservation_form.getText()+"' WHERE NumAvion='"+NumAvion_form.getText()+"' and NumPlace='"+NumPlace_form.getText()+"'");
                                 pst.executeUpdate();
                                 pst.close();
                                 pst=con.prepareStatement("UPDATE PLACE SET Occupation=0, NumReservation='' WHERE NumAvion='"+old_NumAvion+"' and NumPlace='"+old_NumPlace+"'");
                                 pst.executeUpdate();
                                 pst.close();
                            }
                            JOptionPane.showMessageDialog(null, "Mise à jour effectuée");
                            //setVisible(false);
                            JRes.getEditer_btn().setText("EDITER");
                            JRes.getEditer_btn().setEnabled(true);
                            JRes.reservationList();
                            dispose();
                        }catch(Exception e){
                            JOptionPane.showMessageDialog(null, e);
                        }
                    }
                
            }
        }
    }//GEN-LAST:event_Valider_modif_btnActionPerformed

