    private boolean Validation_Avion(){
        boolean ret=true;
        String numAvion=NumAvion_form.getText();
        if(!numAvion.equals("")){
            try{
                pst=con.prepareStatement("SELECT * FROM AVION WHERE NumAvion='"+numAvion+"'");
                rs=pst.executeQuery();
                if(!rs.next()){
                    JOptionPane.showMessageDialog(null, "Le numero de l'avion n'existe pas");
                    ret=false;
                }
                pst.close();
                rs.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            
        }
        return ret;
    }
    
    private boolean Validation_Place(){
        boolean ret=true;
        String numAvion=NumAvion_form.getText();
        String numPlace=NumPlace_form.getText();
        if(!numPlace.equals("")&&!numAvion.equals("")){
            try{
                pst=con.prepareStatement("SELECT * FROM PLACE WHERE NumAvion='"+numAvion+"' and NumPlace='"+numPlace+"'");
                rs=pst.executeQuery();
                if(!rs.next()){
                    JOptionPane.showMessageDialog(null, " Cette place n'existe pas");
                    ret=false;
                }else{
                    if(!old_NumPlace.equals(NumPlace_form.getText())||!old_NumAvion.equals(NumAvion_form.getText())){
                        if(rs.getInt("Occupation")==1){
                            JOptionPane.showMessageDialog(null, "Cette place n'est plus disponible");
                            ret=false;
                        }
                    }
                }
                pst.close();
                rs.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        return ret;
    }
    private boolean already_exist(String num){
        boolean ret=false;
        try{
                pst=con.prepareStatement("SELECT * FROM RESERVATION WHERE NumReservation='"+num+"'");
                rs=pst.executeQuery();
                if(rs.next()){
                    ret=true;
                }
                pst.close();
                rs.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        return ret;
    }

